package 수행테스트;

import java.util.Scanner;

public class 문제41 {
	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		int n = s.nextInt();
		int a = 1;
		for (int i = n - 1; i >= 0; i++) {
			for (int j = 1; j <= j; j++) {
				System.out.print("");
			}
			for (int j = 1; j <= a; j++) {
				System.out.println("*");
			}
			a += 2;
			System.out.println();
		}
	}
}
